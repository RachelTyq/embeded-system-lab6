#ifndef __ARRAYKEY_H_
#define __ARRAYKEY_H_

/*********************************************************************************************************
  Array key 阵列按键控制器类型定义
*********************************************************************************************************/
typedef struct {
    LW_DEV_HDR                  AKEY_devHdr;                            /*  必须是第一个结构体成员      */
    LW_LIST_LINE_HEADER         AKEY_fdNodeHeader;
    PVOID                       AKEY_pvHwPrivate;                       /*  指向私有硬件实体            */
    LW_HANDLE                   AKEY_keyQ;                              /*  消息队列,用于与硬件实体通信 */
    ULONG                       AKEY_ulTimeout;                         /*  超时参数, 默认永久等待      */
} __AKEY_CTRL, *__PAKEY_CTRL;
/*********************************************************************************************************
   功能函数
*********************************************************************************************************/
INT  arrayKeyDrv(VOID);
INT  arrayKeyDevCreate(PCHAR  pcName, PVOID  pvHwPrivate);
VOID  arrayKeyDevRemove(PCHAR  pcName);

#endif                                                                  /*  __ARRAYKEY_H_               */
/*********************************************************************************************************
  END
*********************************************************************************************************/

